num = [2, 5, 7, 9]
print(num)

num[3] = 4 # no lugar do elemento na posição 3, coloque o valor 4
print(num)

num.append(8) # adiciona o valor 8 no final da lista
print(num)

num.sort() # ordena a lista em ordem crescente
print(num)

num.insert(2, 0) # na posição 2, insere o valor 0
print(num)

num.remove(7) # remove o valor 7 da lista
print(num)

num.pop(3) # remove o elemento da posição 3
print(num)